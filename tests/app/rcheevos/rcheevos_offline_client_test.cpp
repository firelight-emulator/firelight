TEST_F(RetroAchievementsOfflineClientTest, PatchDataCachedCorrectly) {
  auto cache = RetroAchievementsCache(":memory:");
  RetroAchievementsOfflineClient offlineClient(cache);

  offlineClient.processResponse("r=patch&g=228", patchData);

  auto response =
      offlineClient.handleRequest("https://retroachievements.com/dorequest.php",
                                  "r=patch&g=228", "application/json");

  ASSERT_NE(nullptr, response.body);
  ASSERT_TRUE(response.body_length > 0);
  ASSERT_EQ(response.http_status_code, 200);

  const auto json = nlohmann::json::parse(patchData);
  const auto patchResponse = json.get<PatchResponse>();

  const auto cachedResponse =
      nlohmann::json::parse(response.body).get<PatchResponse>();

  ASSERT_EQ(cachedResponse.PatchData.ID, patchResponse.PatchData.ID);
  ASSERT_EQ(cachedResponse.PatchData.Title, patchResponse.PatchData.Title);
  ASSERT_EQ(cachedResponse.PatchData.ImageIcon,
            patchResponse.PatchData.ImageIcon);
  ASSERT_EQ(cachedResponse.PatchData.ConsoleID,
            patchResponse.PatchData.ConsoleID);
  ASSERT_EQ(cachedResponse.PatchData.RichPresencePatch,
            patchResponse.PatchData.RichPresencePatch);
  ASSERT_EQ(cachedResponse.PatchData.ImageIconURL,
            patchResponse.PatchData.ImageIconURL);

  ASSERT_EQ(patchResponse.PatchData.Achievements.size(),
            cachedResponse.PatchData.Achievements.size());

  ASSERT_EQ(patchResponse.PatchData.Leaderboards.size(),
            cachedResponse.PatchData.Leaderboards.size());

  for (const auto &achieve : patchResponse.PatchData.Achievements) {
    bool found = false;
    for (const auto &cachedAchieve : cachedResponse.PatchData.Achievements) {
      if (achieve.ID == cachedAchieve.ID) {
        ASSERT_EQ(cachedAchieve.ID, achieve.ID);
        ASSERT_EQ(cachedAchieve.Author, achieve.Author);
        ASSERT_EQ(cachedAchieve.Title, achieve.Title);
        ASSERT_EQ(cachedAchieve.Description, achieve.Description);
        ASSERT_EQ(cachedAchieve.Points, achieve.Points);
        ASSERT_EQ(cachedAchieve.Flags, achieve.Flags);
        ASSERT_EQ(cachedAchieve.Modified, achieve.Modified);
        ASSERT_EQ(cachedAchieve.Created, achieve.Created);
        ASSERT_EQ(cachedAchieve.Rarity, achieve.Rarity);
        ASSERT_EQ(cachedAchieve.Type, achieve.Type);
        ASSERT_EQ(cachedAchieve.BadgeName, achieve.BadgeName);
        ASSERT_EQ(cachedAchieve.MemAddr, achieve.MemAddr);
        ASSERT_EQ(cachedAchieve.RarityHardcore, achieve.RarityHardcore);
        ASSERT_EQ(cachedAchieve.BadgeURL, achieve.BadgeURL);
        ASSERT_EQ(cachedAchieve.BadgeLockedURL, achieve.BadgeLockedURL);

        found = true;
        break;
      }
    }

    ASSERT_TRUE(found);
  }

  for (const auto &leaderboard : patchResponse.PatchData.Leaderboards) {
    bool found = false;
    for (const auto &cachedLeaderboard :
         cachedResponse.PatchData.Leaderboards) {
      if (leaderboard.ID == cachedLeaderboard.ID) {
        ASSERT_EQ(cachedLeaderboard.ID, leaderboard.ID);
        ASSERT_EQ(cachedLeaderboard.Title, leaderboard.Title);
        ASSERT_EQ(cachedLeaderboard.Description, leaderboard.Description);
        ASSERT_EQ(cachedLeaderboard.LowerIsBetter, leaderboard.LowerIsBetter);
        ASSERT_EQ(cachedLeaderboard.Hidden, leaderboard.Hidden);
        ASSERT_EQ(cachedLeaderboard.Mem, leaderboard.Mem);
        ASSERT_EQ(cachedLeaderboard.LowerIsBetter, leaderboard.LowerIsBetter);

        found = true;
        break;
      }
    }

    ASSERT_TRUE(found);
  }
}

